public class atividade3 {

    public static void main(String[] args) {

        int resultado;
        int valorCalculoTabuada = 1;

        while (valorCalculoTabuada <= 10) {

            resultado = valorCalculoTabuada * 5;
            System.out.println(valorCalculoTabuada + " x 5 = " +resultado);
            valorCalculoTabuada++;
        }
    
    }
}
