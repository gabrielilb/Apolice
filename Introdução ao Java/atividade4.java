public class atividade4 {

    public static void main(String[] args) {

        int resultadoSeis;
        int resultadoDois;
        int valorCalculoTabuada = 1;

        while (valorCalculoTabuada <= 10) {

            resultadoSeis = valorCalculoTabuada * 6;
            resultadoDois = valorCalculoTabuada * 2;

            System.out.println(valorCalculoTabuada + " x 6 = " + resultadoSeis + "\t" + valorCalculoTabuada + " x 2 = " + resultadoDois);
            valorCalculoTabuada++;
        }
    
    }
}
